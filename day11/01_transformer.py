# ========================================
# Part 3: Attention 直觉（最重要的概念）
# ========================================

print("=== Part 3: Attention — 谁对谁重要 ===\n")

print("""
Attention 就是一句话：让每个词看到句子中所有其他词，然后自己决定"关注谁"。

举例："我把苹果吃了，因为它很甜"

  没有 Attention：
    "它" 不知道指"苹果"还是"我"

  有 Attention：
    "它" 跟句中每个词算一个"关注度分数"：
      "它"↔"我"   → 0.02  （不太相关）
      "它"↔"苹果" → 0.85  （强相关！"它"指的是"苹果"）
      "它"↔"吃了" → 0.08
      "它"↔"因为" → 0.03
      "它"↔"很甜" → 0.02

    "苹果" 跟句中每个词算关注度：
      "苹果"↔"吃了" → 0.72  （被吃了）
      "苹果"↔"它"   → 0.85  （"它"指"苹果"）
      "苹果"↔"很甜" → 0.65  （苹果很甜）

这样做完 96 层之后，每个词都充分理解了上下文。
""")

# 用一个计算相似度的例子来直观感受 Attention 的思路
import numpy as np

print("--- 用余弦相似度模拟 Attention 的直觉 ---")

# 模拟 4 个词的 Embedding（实际是 512 维，这里用 2 维方便理解）
words = {
    "它": np.array([0.1, 0.2]),
    "我": np.array([0.8, 0.9]),
    "苹果": np.array([0.15, 0.25]),  # 跟"它"很近
    "很甜": np.array([0.3, 0.4]),
}

target = "它"
target_vec = words[target]

print(f"\n「{target}」的向量: ({target_vec[0]:.1f}, {target_vec[1]:.1f})")
print(f"它跟句中其他词的相似度:")

for word, vec in words.items():
    if word == target:
        continue
    sim = np.dot(target_vec, vec) / (np.linalg.norm(target_vec) * np.linalg.norm(vec))
    bar = "█" * int(sim * 20)
    print(f"  「{target}」↔「{word}」: {sim:.4f} {bar}")

print("\nAttention 做的事情本质上就是这个——不过是更复杂的版本。")
print("它用可训练的权重矩阵（Q/K/V）来计算'谁对谁重要'，而不是简单的余弦相似度。")


# ========================================
# Part 4: 三种 Transformer 架构
# ========================================

print("\n=== Part 4: Encoder-only / Decoder-only / Encoder-Decoder ===\n")

print("""
┌──────────────────┬───────────────┬──────────────┬────────────────────┐
│      架构         │   只看上文     │   看上下文    │    典型模型          │
├──────────────────┼───────────────┼──────────────┼────────────────────┤
│ Encoder-only      │     否        │      是       │  BERT              │
│ Decoder-only      │     是        │      否       │  GPT / 通义千问     │
│ Encoder-Decoder   │     否        │      是       │  T5 / 翻译模型      │
└──────────────────┴───────────────┴──────────────┴────────────────────┘

【Encoder-only — BERT】
  - 能看到"前面"和"后面"的词（双向）
  - 适合：理解任务。分类（情感分析）、实体识别、问答理解
  - 不能生成文本（它没有"预测下一个词"的能力）
  
  例子: "我__苹果" → BERT 前后都看 → 猜中间是"吃"

【Decoder-only — GPT / 所有聊天模型】
  - 只能看"前面"的词（单向，这是因果掩码的效果）
  - 适合：生成任务。对话、写作、代码生成
  - 你用的 GPT-4 / DeepSeek / 通义千问 都是这类
  
  例子: "我爱" → 只能根据"我"和"爱" → 猜下一个是"你"

  为什么生成只能用 Decoder-only？
    因为它训练的方式就是"根据前面的词预测下一个"，
    所以天然适合"一个接一个地生成文字"。

【Encoder-Decoder — T5 / BART】
  - Encoder 读完整输入（双向），Decoder 生成输出（单向）
  - 适合：翻译、摘要（输入和输出是不同内容）
  
  例子: 输入"我爱中国" → Encoder理解整句 → Decoder逐个生成 "I love China"

""")


# ========================================
# Part 5: Q/K/V — Attention 的运算核心（了解即可）
# ========================================

print("=== Part 5: Q/K/V 是什么 ===\n")

print("""
每个词的向量乘以三个不同的权重矩阵，得到三个向量：

  Q（Query，查询）: "我在找什么？"
  K（Key，键）:     "我是什么？"
  V（Value，值）:   "我有什么内容？"

用"它"的例子：
  "它"的 Q × "苹果"的 K → 发现高度匹配 → 说明"它"想知道"苹果"的信息
  → 所以"它"大量采纳"苹果"的 V（即"苹果"这个词的实际内容）

  "它"的 Q × "我"的 K → 匹配度很低 → 几乎不采纳"我"的内容

Self-Attention 就是：
  每个词用 Q 去匹配所有词的 K → 得到匹配分数
  → 按分数加权取所有词的 V → 得到"融合了上下文信息的新表示"

这就是 Attention 层做的事情。重复 96 层后，每个词都深度理解了整句话。
""")


